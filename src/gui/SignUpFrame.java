     public boolean validateInputs()
    {
        if(txtName.getText().isEmpty() || txtMail.getText().isEmpty() || txtPhone.getText().isEmpty() || txtPwd.getText().isEmpty() || txtCnfPwd.getText().isEmpty()
 || skill1Txt.getText().isEmpty() || qualificationTxt.getText().isEmpty()|| skill3Txt.getText().isEmpty()|| skill4Txt.getText().isEmpty()|| filePathTxt.getText().isEmpty())
             return false;
        return true;
    }
    
    public boolean pwdInputs()
    {
        if(txtPwd.getText().trim().equalsIgnoreCase(txtCnfPwd.getText().trim()))
             return true;
        return false;
    }

