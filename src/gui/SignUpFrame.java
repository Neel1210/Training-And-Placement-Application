    private void createAccBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccBtnActionPerformed
        // TODO add your handling code here:
        if(validateInputs())
        {
        
           
            ParticipantsPojo pt=new ParticipantsPojo();
            pt.setpId(pIdLbl.getText().trim());
            pt.setUserId(txtMail.getText().toUpperCase().trim());
            pt.setName(txtName.getText().trim());
            pt.setPhone(txtPhone.getText().trim());
            pt.setResume(new File(filePathTxt.getText().trim()));
            pt.setSkills(skill1Txt.getText().trim()+","+skill2Txt.getText().trim()+","+skill3Txt.getText().trim()+","+skill4Txt.getText().trim());
            pt.setQualifications(qualificationTxt.getText().trim());
            
            if(pwdInputs())
            {
                pt.setPassword(txtPwd.getText().trim());
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Password & Confrim Password aren't same !!!","Warning",JOptionPane.INFORMATION_MESSAGE);
                return; 
            }
            
            if(ParticipantsDao.addNewParticipants(pt))
            {
                JOptionPane.showMessageDialog(null,"Participants Added Succefully","Success",JOptionPane.INFORMATION_MESSAGE);
                txtMail.setText("");
                txtName.setText("");
                txtPhone.setText("");
                filePathTxt.setText("");
                skill1Txt.setText("");
                skill2Txt.setText("");
                skill3Txt.setText("");
                skill4Txt.setText("");
                qualificationTxt.setText("");
                txtPwd.setText("");
                txtCnfPwd.setText("");
                reloadId();
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(null,"DB-error while adding particpant","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        
        
            
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please fill all fields","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_createAccBtnActionPerformed

